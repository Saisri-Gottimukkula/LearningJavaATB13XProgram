package ex_06_for_loop;

public class Lab005_For_IfLoop {
    public static void main(String[] args) {
        for(int sree=0;sree<18;sree++){
            if(sree>15){
                System.out.println("She will get a gift : iphone");
            }
            else{
                System.out.println("she will get candy and hugs");
            }
        }
    }
}
