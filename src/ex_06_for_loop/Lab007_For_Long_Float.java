package ex_06_for_loop;

public class Lab007_For_Long_Float {
    public static void main(String[] args) {
        for(long i=1l;i<11;i++){
            System.out.println(i);
        }
        for(float f = 0.12f;f>10.67;f++){
            System.out.println(f);
        }
    }
}
